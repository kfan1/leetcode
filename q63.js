/**
 * @param {number[][]} obstacleGrid
 * @return {number}
 */
var uniquePathsWithObstacles = function (obstacleGrid) {
  let paths = 0;
  let xMax = obstacleGrid[0].length - 1;
  let yMax = obstacleGrid.length - 1;

  (function helper(x = 0, y = 0) {
    if (x === xMax && y === yMax) return paths++;
    if (x + 1 <= xMax && obstacleGrid[y][x + 1] !== 1) helper(x + 1, y);
    if (y + 1 <= yMax && obstacleGrid[y + 1][x] !== 1) helper(x, y + 1);
  })();

  return paths;
};

